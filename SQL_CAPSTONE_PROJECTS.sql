--13.	Write a query to retrieve the customers who have placed orders for every month of a specific year

SELECT C.CustomerID , C.Name 
FROM Customers AS C
WHERE NOT EXISTS 
(SELECT 1 FROM 
  (SELECT DISTINCT DATEPART(month , OrderDate) AS Month 
        FROM Orders WHERE DATEPART(YEAR , OrderDate)=2023) AS M 
            WHERE NOT EXISTS(
                SELECT 1 FROM Orders AS O WHERE O.CustomerID = C.CustomerID AND DATEPART(Month , O.OrderDate)= M.MONTH
));



